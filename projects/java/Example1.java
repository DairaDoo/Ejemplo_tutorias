package projects.java;
public class Example1 
{
    public static void main ( String[] args )
    {
        int valor1 = 10;
        double valor2 = 20; // double más preciso.
        float valor3 = 30; // float menos preciso.
        char valor4 = 'S'; // Un caracter o número.
        boolean valor5 = true; // cierto o falso.
        long valor6 = 100000; // tiene un rango más amplio de números.
        byte valor7 = 127; // de -128 a 127.
        short valor8 = 3200; // –32,768 to 32,767
        String valor9 = "Hola Mundo";

        System.out.println(valor1);
        System.out.println(valor2);
        System.out.println(valor3);
        System.out.println(valor4);
        System.out.println(valor5);
        System.out.println(valor6);
        System.out.println(valor7);
        System.out.println(valor8);
        System.out.println(valor9);



       


    }    
}
